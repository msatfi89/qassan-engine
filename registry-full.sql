INSERT INTO places (level, parent_id, name_ar)
SELECT 'delegation', gov.id, v.name_ar
FROM v JOIN gov ON gov.name_ar = v.gov_ar
WHERE NOT EXISTS (
  SELECT 1 FROM places p
  WHERE p.level = 'delegation' AND p.parent_id = gov.id AND p.name_ar = v.name_ar
);

